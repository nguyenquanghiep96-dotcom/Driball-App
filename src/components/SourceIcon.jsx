export function SourceIcon({ source, size = 14 }) {
  const s = SOURCES.find(src => src.key === source);
  if (!s) return null;
  return s.icon(size);
}

export function SourcePicker({ value, onChange }) {
  return (
    <div className="form-row">
      <label>Nguồn KH</label>
      <div style={{ display: 'flex', gap: 8, marginLeft: 'auto' }}>
        {SOURCES.map(s => {
          const isSelected = value === s.key;
          return (
            <button
              key={s.key}
              type="button"
              onClick={() => onChange(isSelected ? '' : s.key)}
              style={{
                width: 36, height: 36, borderRadius: 10,
                background: isSelected ? `${s.color}18` : 'var(--color-bg-tertiary)',
                border: isSelected ? `2px solid ${s.color}` : '2px solid transparent',
                display: 'flex', alignItems: 'center', justifyContent: 'center',
                cursor: 'pointer', transition: 'all 0.2s',
                opacity: isSelected ? 1 : 0.5,
              }}
              title={s.label}
            >
              {s.icon(18)}
            </button>
          );
        })}
      </div>
    </div>
  );
}

